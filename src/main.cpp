#include <Arduino.h>
#include "sevSeg/sevSeg.h"

sevSeg Display;

/***********************/

//DEBUG:
const bool debug = 0;

float x;

unsigned long Mill = millis();

//-----------------------------------------------------------//
///////////////////////////////////////////////////////////////
//-----------------------------------------------------------//

void setup()
{

  if (debug)
  {
    Serial.begin(115200);
  }

  Display.init();
}

//-----------------------------------------------------------//

void loop()
{

  char Tmp[100];

  String TmpStr = String(1.1 * x);
  x = x + 0.00001;
  TmpStr.toCharArray(Tmp, 100);
  Display.printChar(Tmp);

  Display.multiplex();
}

